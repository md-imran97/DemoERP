package com.view.model;

public class EmployeeAndTeam {

	private int employeeId;
	private String employeeName;
	private int teamRole;
	private int teamActivity;
	
	public int getEmployeeId() {
		return employeeId;
	}
	public void setEmployeeId(int employeeId) {
		this.employeeId = employeeId;
	}
	public String getEmployeeName() {
		return employeeName;
	}
	public void setEmployeeName(String employeeName) {
		this.employeeName = employeeName;
	}
	public int getTeamRole() {
		return teamRole;
	}
	public void setTeamRole(int teamRole) {
		this.teamRole = teamRole;
	}
	public int getTeamActivity() {
		return teamActivity;
	}
	public void setTeamActivity(int teamActivity) {
		this.teamActivity = teamActivity;
	}
	
	
}
