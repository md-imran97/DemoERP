package com.entity;

public class Development {

	private int developmentId;
	private int teamId;
	private int employeeId;
	private int employeeRole;
	private int developmentStatus;
	
	public int getDevelopmentId() {
		return developmentId;
	}
	public void setDevelopmentId(int developmentId) {
		this.developmentId = developmentId;
	}
	public int getTeamId() {
		return teamId;
	}
	public void setTeamId(int teamId) {
		this.teamId = teamId;
	}
	public int getEmployeeId() {
		return employeeId;
	}
	public void setEmployeeId(int employeeId) {
		this.employeeId = employeeId;
	}
	public int getEmployeeRole() {
		return employeeRole;
	}
	public void setEmployeeRole(int employeeRole) {
		this.employeeRole = employeeRole;
	}
	public int getDevelopmentStatus() {
		return developmentStatus;
	}
	public void setDevelopmentStatus(int developmentStatus) {
		this.developmentStatus = developmentStatus;
	}
	
	
}
